package uelbosque.lerni.model;



public class Tipo_usuario {

	private Long cod_tipo_usuario;
	private String descripcion;
	
	
	public Long getCod_tipo_usuario() {
		return cod_tipo_usuario;
	}
	public void setCod_tipo_usuario(Long cod_tipo_usuario) {
		this.cod_tipo_usuario = cod_tipo_usuario;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	
}
